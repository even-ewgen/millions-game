    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!helpAll) {
            helpAll = true;
        JButton[] btns = new JButton[]{answert1, answert2, 
            answert2, answert4};
        int[] counters = new int[4];
        int voices = 100;
        for (int i = 0; i < btns.length; i++) {
           if (currentQuestion.RightAnswer.equals(btns[i].getActionCommand())) {
               System.out.println(currentQuestion.RightAnswer.toString());
               counters[i] = rnd.nextInt(voices+1);
               voices = voices - counters[i];
           }
        }
        for (int i = 0; i < counters.length; i++) {
            if (voices != 0) {
                if (counters[i] == 0) {
                    counters[i] = rnd.nextInt(voices+1);
                    voices = voices - counters[i];
                }
            } else {
                counters[i] = 0;
            }
        }
      
        JOptionPane.showMessageDialog(this, String.format("%s - %d %s - %d %s - %d %s - %d", 
                btns[0].getText(), counters[0], btns[1].getText(), counters[1], 
                btns[2].getText(), counters[2], btns[3].getText(), counters[3]));
        } else JOptionPane.showMessageDialog(this, "Только один раз)");
    }//GEN-LAST:event_jButton2ActionPerformed

