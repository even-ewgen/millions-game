    private void ReadFile()
    {
        try{
            FileInputStream fstream = new FileInputStream("Вопросы.txt");
            BufferedReader br = new BufferedReader(new InputStreamReader(fstream));
            String strLine;

            while ((strLine = br.readLine()) != null) {
                String[] s = strLine.split("\t");
                questions.add(new Question(s));
            }
           
        } catch (IOException e) {
            System.out.println("Ошибка");
        }
    }
    
    private void ShowQuestion(Question q)
    {
        questionText.setText(q.Text);
        answert1.setText(q.Answers[0]);
        answert2.setText(q.Answers[1]);
        answert3.setText(q.Answers[2]);
        answert4.setText(q.Answers[3]);
    }
    
   private Question GetQuestion(int level)
    {
        List<Question> list = questions.stream().filter(q->q.Level==level).collect(Collectors.toList());
        return list.get(rnd.nextInt(list.size()));
    }
    
     private void NextStep()
    {
        JButton[] btns = new JButton[]{answert1, answert2, 
            answert3, answert4};
        
        for(JButton btn: btns)
            btn.setEnabled(true);
        
        Level++;
        currentQuestion = GetQuestion(Level);
        ShowQuestion(currentQuestion);
        jList1.setSelectedIndex(jList1.getModel().getSize()-Level);
    }
     
    private void startGame()
    {
        help50 = false;
        helpAll = false;
        helpFrend = false;
        Level = 0;
        NextStep();
    }

    
